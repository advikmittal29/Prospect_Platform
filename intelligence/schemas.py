# Outreach message schemas
# ---------------------------------------------------------------------------

class OutreachMessage(BaseModel):
    """A generated outreach message for a specific prospect."""
    profile_url: str
    name: Optional[str]
    channel: str = Field(description="linkedin_connect | linkedin_message | email")

    subject: Optional[str] = Field(
        default=None, description="Subject line (email only)"
    )
    message_body: str = Field(description="Full message text")
    word_count: int = Field(ge=0)
    tone: str = Field(description="professional | warm | direct | consultative")

    personalization_hooks: List[str] = Field(
        default_factory=list,
        description="Specific personalisation elements used"
    )
    suggested_followup: Optional[str] = Field(
        default=None, description="Suggested follow-up action if no reply"
    )

    @field_validator("message_body")
    @classmethod
    def _set_word_count(cls, v: str) -> str:
        return v  # word_count set separately after init

    def compute_word_count(self) -> None:
        self.word_count = len(self.message_body.split())


class OutreachBatch(BaseModel):
    """Batch of outreach messages for a company."""
    company_name: str
    messages: List[OutreachMessage] = Field(default_factory=list)
    generated_at: str
    model_used: str
